const toTitle = (slug: string) => {
  const filename = slug.replace('Storytime-audio-files/', '').replace('.mp4', '');
  let title = filename.replace(/---/g, "'").replace(/--/g, '-').replace(/-/g, ' ');
  title = title.replace(/\s+/g, ' ').trim();
  // Title-case while keeping common "'s" lowercased.
  title = title
    .split(' ')
    .map(word => {
      if (word.length === 0) return word;
      const first = word.charAt(0).toUpperCase();
      const rest = word.slice(1);
      return first + rest;
    })
    .join(' ')
    .replace(/'S\b/g, "'s");
  return title;
};

export const stories: Story[] = AUDIO_KEYS.map((key, idx) => ({
  id: idx + 1,
  title: toTitle(key),
  url: `/api/audio?key=${encodeURIComponent(key)}`,
  duration: '0:00',
}));

export const storiesSorted: Story[] = [...stories].sort((a, b) => a.title.localeCompare(b.title));
